<p style="text-align:center; line-height:1.5; color:#888;">To make HOPE accessible to the public easily we run a Helpline from 9am – 9pm for distressed strays who needs medical assistance. This service is supported by the three Friendicoes ambulances which pick up the injured or sick animal from it’s area and brings it to the shelter for treatment. Once treatment and recuperation is complete the animal is again dropped back to its home area by these ambulances. The Helpline receives 50 calls on an average every day.
While three ambulances are in no way capable of attending to every one of these calls due to obstacles like time constraints and traffic situation, we do our best to reach out to as many animals we can. Hit and runs, maggot wounds and sick animals are the top most priority. Unfortunately the Friendicoes night clinic and night ambulance service have been indefinitely suspended due to financial constraints. We are looking for funding to help us re-start the Night Ambulance. If you would like to sponsor this programme please get in contact with us. </p>
