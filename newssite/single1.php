<p>
Goods and Services Tax (GST) is an indirect tax applicable throughout India which replaced
multiple cascading taxes levied by the central and state governments. It was introduced as 
The Constitution (One Hundred and First Amendment) Act 2017,following the passage of Constitution
122nd Amendment Bill. The GST is governed by a GST Council and its Chairman is the Finance Minister 
of India. Under GST, goods and services are taxed at the following rates, 0%, 5%, 12% and 18%. There is 
a special rate of 0.25% on rough precious and semi-precious stones and 3% on gold. In addition a cess of 
15% or other rates on top of 28% GST applies on few items like aerated drinks, luxury cars and tobacco products.
Touted by the government to be India's biggest tax reform in 70 years of independence, the Goods and Services
Tax (GST) was finally launched on the midnight of 30 June 2017, though the process of forming the legislation took 
17 years (since 2000 when it was first proposed). The launch was marked by a historic midnight
(30 June - 1 July 2017) session of both the houses of parliament convened at the Central Hall of the Parliament, 
but which was immediately boycotted by the opposition by staging a walk out to show their disapproval of the same.
